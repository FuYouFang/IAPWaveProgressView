import UIKit

extension Int {
    var cf: CGFloat {
        return CGFloat(self)
    }
    
    var f: Float {
        return Float(self)
    }
}

extension Float {
    var cf: CGFloat {
        return CGFloat(self)
    }
}

extension CGFloat {
    var f: Float {
        return Float(self)
    }
}

extension Double {
    var cf: CGFloat {
        return CGFloat(self)
    }
}
